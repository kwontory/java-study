package operator;

public class Comp1 {

    public static void main(String[] args) {
        // 비교 연산자: 두 값을 비교하는데 사용한다.
        // ==, !=, >, <, >=, <=
        int a = 2;
        int b = 3;
        System.out.println(a == b);
        boolean isEqual = a == b;
        System.out.println(isEqual);
    }

}
