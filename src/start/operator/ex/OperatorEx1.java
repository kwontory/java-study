package start.operator.ex;

public class OperatorEx1 {
    public static void main(String[] args) {
        // 문제1 - int와 평균
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        int sum = num1 + num2 + num3;
        int average = sum / 3;
        System.out.println("합계: "  + sum + ", 평균: " + average);
    }
}
