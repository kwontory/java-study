package start.variable;

public class Var7 {
    public static void main(String[] args) {
        // 변수 타입: 다루는 데이터의 종류에 따라 다양한 형식(형)이 존재한다.
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, false
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열(클래스)
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // 리터럴(literal): 고정된 값(100, 10.5, true, A, Hello Java 등..
    }
}
